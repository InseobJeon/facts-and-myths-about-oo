export class Calculator {
  constructor() {}

  public plus(number1: number, number2: number): number {
    return number1 + number2
  }

  public minus(number1: number, number2: number): number {
    return number1 - number2
  }

  public multiple(number1: number, number2: number): number {
    return number1 * number2
  }

  public divide(number1: number, number2: number): number {
    return number1 / number2
  }
}
